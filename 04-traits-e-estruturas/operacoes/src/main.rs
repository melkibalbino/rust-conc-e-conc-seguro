fn main() {
    println!("1 + 2 = {}", 1 + 2);
    println!("1.3 + 2.4 = {}\n", 1.3 + 2.4);

    println!("1 - 2 = {}", 1 - 2);
    println!("1.3 - 2.4 = {}\n", 1.3 - 2.4);

    println!("1 * 2 = {}", 1 * 2);
    println!("1.3 * 2.4 = {}\n", 1.3 * 2.4);

    println!("1 / 2 = {}", 1 / 2);
    println!("1.3 / 2.4 = {}\n", 1.3 / 2.4);

    println!("1 % 2 = {}", 1 % 2);
    println!("1.3 % 2.4 = {}\n", 1.3 % 2.4);

    println!("!false = {}", !false);
    println!("!3 = {}\n", !3);

    println!("true & false = {}", true & false);
    println!("2 & 6 = {}\n", 2 & 6);

    println!("true | false = {}", true | false);
    println!("12 | 5 = {}\n", 12 | 5);

    println!("true ^ false = {}", true ^ false);
    println!("12 ^ 5 = {}\n", 12 ^ 5);

    println!("12 << 5 = {}\n", 12 << 5);
    println!("12 >> 5 = {}\n", 12 >> 5)
}
